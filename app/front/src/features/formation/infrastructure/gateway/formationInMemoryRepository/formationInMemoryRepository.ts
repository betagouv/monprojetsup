  public async rechercher(recherche: string): Promise<FormationAperçu[] | undefined> {
    return new Promise((resolve) => {
      setTimeout(() => {
        const fuse = new Fuse<Formation>(this.FORMATIONS, {
          distance: 200,
          threshold: 0.4,
          keys: ["nom"],
        });

        const correspondances = fuse.search(recherche);
        resolve(correspondances.map((correspondance) => this._mapperVersAperçu(correspondance.item)));
      }, 300);
    });
  }

  public async récupérerAperçus(formationIds: Array<FormationAperçu["id"]>): Promise<FormationAperçu[] | undefined> {
    return this.FORMATIONS.filter((formation) => formationIds.includes(formation.id)).map(this._mapperVersAperçu);
  }

  private _mapperVersAperçu = (formation: Formation) => {
    return {
      id: formation.id,
      nom: formation.nom,
    };
  };
